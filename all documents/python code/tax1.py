        x=input("Өтінемін жалақыңызды енгізіңіз: ")
        if len(set(x)-sett)!=0:
            print("/Қате шықты/ қайта енігізіңіз ")
        elif '.' in x:
            print("/Қате шықты/ қайта енігізіңіз ")
        else:
            s.append(int(x))
