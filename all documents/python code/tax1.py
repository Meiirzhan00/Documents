        print("\nҚызметкер жалақысы: {}\t opv={}\t ipn={}\t so={}\t sn={}\t ms={}\t Компания налогы: {} ".format(s,int(opv),int(ipn),int(so),int(sn),int(ms),int(Company.nald)))
            
